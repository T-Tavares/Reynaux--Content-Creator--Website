/* 
    To keep the code tidy the database have it's own functions    
    Those functions are reponsible for any change on the databases.

    Any direct change to the DB should be managed by functions stored
    on this file.

    I've used the Javascript convention for private fields and methods, 
    that is to put '_' in front of functions and variables. It also makes 
    easy to  differentiate the DB functions from the helpers and actions 
    functions since some of them have similar name and purposes
*/

export function _addToCartDB(obj) {
    cartDB.push(obj);
}

export function _removeFromCartDB(objRef) {
    const updatedCart = cartDB.filter(item => item.name !== objRef);
    cartDB = updatedCart;
}

export function _getCartTotalDB(isPromoValid = false) {
    let totalPrice = cartDB.reduce((acc, currVal) => currVal.price + acc, 0);
    if (isPromoValid) return (totalPrice *= 0.85); // 15% OFF
    return totalPrice;
}

